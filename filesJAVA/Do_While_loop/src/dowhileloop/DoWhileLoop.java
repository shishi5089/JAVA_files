package dowhileloop;

public class DoWhileLoop
{
	public static void main(String[]args)
	{
		int x=0;
		
		do
		{
			System.out.println("You are printing the value of "+x);
			x++;
			
		}
		while(x<10);
		
		int y=0;
		
		while(y<10)
		{
			y++;
			System.out.println("You are printing the value of the "+y);
			
		}
	}

}
